    private void populateCombo() {
        
        orgcmbox.removeAllItems();
        System.out.println(enterprise.getEnterpriseType());
        if(enterprise.getEnterpriseType().equals(LegalDepartment)){
        for(Organization.LegalType legalType: Organization.LegalType.values()){
            if (legalType.getValue().equals(Organization.LegalType.Advocate.getValue())){
                orgcmbox.addItem(legalType);
            }
        }
        }
        else if(enterprise.getEnterpriseType().equals(DeliveryDepartment)){
        for(Organization.DeliveryType DeliveryType: Organization.DeliveryType.values()){
            if (DeliveryType.getValue().equals(Organization.DeliveryType.Delivery.getValue())){
                orgcmbox.addItem(DeliveryType);
            }
        }
        }
        else if(enterprise.getEnterpriseType().equals(LimbsBank)){
        for(Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.LimbsManufacturer.getValue())
                    || type.getValue().equals(Organization.Type.Coordinator.getValue()) ||
                    type.getValue().equals(Organization.Type.Doctor.getValue()) ||
                    type.getValue().equals(Organization.Type.Delivery.getValue())){
                orgcmbox.addItem(type);
            }
        }
        }
        else if(enterprise.getEnterpriseType().equals(HospitalDepartment)){
        for(Organization.HospitalType HospitalType: Organization.HospitalType.values()){
            if (HospitalType.getValue().equals(Organization.HospitalType.Doctor.getValue())){
                orgcmbox.addItem(HospitalType);
            }
        }
        }else{
        for (Organization.Type type : Organization.Type.values()){            
            if (type.getValue().equals(Organization.Type.Coordinator.getValue())
                    ||type.getValue().equals(Organization.Type.Doctor.getValue())
                    ||type.getValue().equals(Organization.Type.InventoryManager.getValue())
                    ||type.getValue().equals(Organization.Type.Lab.getValue()) || type.getValue().equals(Organization.Type.LimbsManufacturer.getValue()))
                orgcmbox.addItem(type);
        }
    }
    }
private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[3];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            row[2] = organization.getOrgName();
            
            model.addRow(row);
        }
    }
