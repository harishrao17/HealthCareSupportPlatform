    private void addOrganizationbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrganizationbtnActionPerformed
        // TODO add your handling code here:
        //directory.createDonorBankOrganization((Type)organizationComboBox.getSelectedItem());
        
        
        if (organizationnmlbl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Organization name cannot be empty");
            return;
        }

        for (Organization organization : directory.getOrganizationList()) {

            if (organization.getOrgName().equals(organizationnmlbl.getText())) {

                JOptionPane.showMessageDialog(null, "Organization name already Exists");
                return;
            }
            
          

        }

        if(enterprise.getEnterpriseType().equals(LegalDepartment)){
            Organization o = directory.createLegalOrganization((LegalType)orgcmbox.getSelectedItem());
            o.setOrgName(organizationnmlbl.getText());
            populateTable(); 
            JOptionPane.showMessageDialog(null, "Organization created ");
        }
        else if(enterprise.getEnterpriseType().equals(DeliveryDepartment)){
            Organization o = directory.createDeliveryOrganization((DeliveryType)orgcmbox.getSelectedItem());
            
            
            o.setOrgName(organizationnmlbl.getText());
            populateTable(); 
            JOptionPane.showMessageDialog(null, "Organization created ");
        }
        else if(enterprise.getEnterpriseType().equals(HospitalDepartment)){
            Organization o = directory.createHospitalOrganization((HospitalType)orgcmbox.getSelectedItem());
            o.setOrgName(organizationnmlbl.getText());
            populateTable(); 
            JOptionPane.showMessageDialog(null, "Organization created ");
        }
        else if(enterprise.getEnterpriseType().equals(LimbsBank)){
            Organization o = directory.createLimbsManufacturerOrganization((Type)orgcmbox.getSelectedItem());
            System.out.println(organizationnmlbl.getText());
            o.setOrgName(organizationnmlbl.getText());
            populateTable();
            JOptionPane.showMessageDialog(null, "Organization created ");
        }
        else{
            Organization o = directory.createDonorBankOrganization((Type)orgcmbox.getSelectedItem());
            o.setOrgName(organizationnmlbl.getText());
            JOptionPane.showMessageDialog(null, "Organization created ");
            orgcmbox.removeItem(orgcmbox.getSelectedItem());
            populateTable();
        }
        
        organizationnmlbl.setText("");
    }//GEN-LAST:event_addOrganizationbtnActionPerformed

