    private void btncreateentpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateentpActionPerformed
        // TODO add your handling code here:
        NGO ngo = (NGO) comboxntw.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) comboxentptype.getSelectedItem();
        
        if (ngo == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        
        for (Enterprise enterprise : ngo.getEnterpriseDirectory().getEnterpList()) {
            if (enterprise.getEnterpriseType() == type ) {
                JOptionPane.showMessageDialog(null, "Enterbrise type alreday exist in organization!");
                return;
            }
        }
        
//        try {
            //contactField.getText();
            //zipCode.getText();

//        } catch (NumberFormatException e) {
//            JOptionPane.showMessageDialog(null, "Contact Number and Zip code should be numeric", "Error", 0);
//            return;
//        }
        String pattern = ".*\\D.*";
        Pattern patternForZip = Pattern.compile(pattern);
        String zipCode1 = txtzcode.getText();
        String contact = txtcont.getText();
        Matcher matcherForZip = patternForZip.matcher(zipCode1);
         Matcher matcherForContact = patternForZip.matcher(contact);
        if (matcherForZip.matches() == true || matcherForContact.matches() == true) {
            JOptionPane.showMessageDialog(null, "Contact Number and Zip code should be numeric", "Error", 0);
			return;
        }
        
        
        if (txtcont.getText().length() != 10 || txtzcode.getText().length() != 5){
           JOptionPane.showMessageDialog(null, "Contact number should be 10 digit and Zip should be 5 digit" , "Error", JOptionPane.ERROR_MESSAGE);
           return;
        }
       System.out.println(ngo.getEnterpriseDirectory().getEnterpList());
       System.out.println(1);
       for (Enterprise ent : ngo.getEnterpriseDirectory().getEnterpList()) {
       System.out.println(ent.getName());
       if (ent.getName() != null && ent.getName().equals(txtname.getText())) {
           JOptionPane.showMessageDialog(null, "Enterprise name cannot be same" , "Error", JOptionPane.ERROR_MESSAGE);
           return;
       
       }
       
       }

        String name = txtname.getText();

        Enterprise enterprise = ngo.getEnterpriseDirectory().createAndAddEnterprise(name, type);
        enterprise.setAddress(txtzcode.getText());
        enterprise.setPhoneNumber(txtcont.getText());
        
        enterprise.setName(name);
        JOptionPane.showMessageDialog(null, "Enterprise " + name + " is created sucessfully");
        clearfields();

        populateTable();
    }//GEN-LAST:event_btncreateentpActionPerformed

