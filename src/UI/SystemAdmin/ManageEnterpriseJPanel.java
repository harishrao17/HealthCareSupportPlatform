    private void populateDefault() {

        comboxntw.removeAllItems();
        comboxentptype.removeAllItems();
        for (NGO ngo : ecosystem.getngoList()) {
            if (useraccount.getUsername().equals(ngo.getName()) || useraccount.getUsername().equals("sysadmin")) {
                comboxntw.addItem(ngo);
            }
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {

            comboxentptype.addItem(type);
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblentp.getModel();

        model.setRowCount(0);
        for (NGO ngo : ecosystem.getngoList()) {
            System.out.println(ngo);
            System.out.println(ngo.getEnterpriseDirectory().getEnterpList());
            if (useraccount.getUsername().equals(ngo.getName()) || useraccount.getUsername().equals("sysadmin")) {
                if (ngo.getEnterpriseDirectory().getEnterpList() != null) {
                    for (Enterprise enterprise : ngo.getEnterpriseDirectory().getEnterpList()) {
                        Object[] row = new Object[5];
                        row[0] = ngo.getName();
                        row[1] = enterprise.getEnterpriseType().getValue();
                        row[2] = enterprise;
                        row[3] = enterprise.getPhoneNumber();
                        row[4] = enterprise.getAddress();

                        model.addRow(row);
                    }
                }
            }
        }
    }

